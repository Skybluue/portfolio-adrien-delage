    <h3>Greeneye Beta</h3><br>
    Application mobile visant a être utilisée sur tablette et dont le but est de pouvoir effectuer des relevés agronomiques.<br>
    Ces relevés sont ensuite consultables sur un site web (réalisé par un collègue). <br>
    Cet outil n'a pas pour vocation d'être commercialisé, mais d'être utilisé par des employés de l'entreprise afin de suivre l'évolution des maladies sur une parcelle de culture de betteraves. <br>
    L'application a été réalisée en Dart (avec le framework Flutter) et utilise une base de données Firestore.<br>
