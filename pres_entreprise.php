    Greenshield est une start-up Française créée en 2016 par François Feugier et Jean-Pierre Plonquet. <br>
    La création de l'entreprise repose au départ sur le module laser dont l'idée est de détruire les pestes <br> 
    (bioagresseurs comme insectes (pucerons porteurs de la jaunisse) ou mauvaises herbes) : désherbage de précision utilisant un laser de puissance (comme un laser de découpe). <br>
    Ils ont ensuite fait appel à l'ANR (Agence Nationale de Recherche) et l'INSA de Lyon (où se trouve le siège historique) afin de pouvoir monter le module laser sur un robot. <br>
    Le projet fut en pause jusqu'en 2018, année pendant laquelle on eu lieu les premiers recrutements (un ingénieur agronomme et un data scientist). <br><br>

    "L'intelligence artificielle au service de l'agronomie et d'un itinéraire cultural durable." <br>
    "Greenshield développe des solutions agronomiques, technologiques et mathématiques permettant de réduire l’usage de phytosanitaires en maintenant le niveau de rendement." <br><br>

    Aujourd'hui, Greenshield est en pleine expansion, une vague de recrutement a eu lieu et les projets et partenaires se multiplient. <br>
    L'équipe est maintenant constituée de 15 personnes, et une offre de suivi sanitaire, une offre de suivi d'expérience et une offre d'optimisation de traitements seront bientôt disponibles. <br> <br>
